function E = InverseVDP(I12)

%     a = 0.0013;
%     b = 2.4969e-16;
%     c = 5.8825;
%     d = 232.25;
%     e = 1.6425;
%     f = 0.0055;
%     ll = 47.568;
%     lh = 836.59;

    a = 0.0569;
    b = 7.3014e-30;
    c = 9.9872;
    d = 884.17;
    e = 32.994;
    f = 0.0047811;
    ll = 98.381;
    lh = 1204.7;
    
    E = zeros(size(I12));
    E(I12 < ll) = a * I12(I12 < ll);
    E(I12 >= ll & I12 < lh) = b * (I12(I12 >= ll & I12 < lh) + d).^c;
    E(I12 >= lh) = e * exp(f * I12(I12 >= lh));
    
end