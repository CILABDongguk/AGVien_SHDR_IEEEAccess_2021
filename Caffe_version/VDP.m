function I12 = VDP(E)
%     a = 769.18;
%     b = 449.12;
%     c = 0.169;
%     d = -232.25;
%     e = 181.7;
%     f = -90.160;
%     yl = 0.0618;
%     yh = 164.10;

    a = 17.554;
    b = 826.81;
    c = 0.10013;
    d = -884.17;
    e = 209.16;
    f = -731.28;
    yl = 5.6046;
    yh = 10469;
    
    I12 = zeros(size(E));
    I12(E < yl) = a * E(E < yl);
    I12(E >= yl & E < yh) = b * (E(E >= yl & E < yh).^c) + d;
    I12(E >= yh) = e * log(E(E >= yh)) + f;
    
end